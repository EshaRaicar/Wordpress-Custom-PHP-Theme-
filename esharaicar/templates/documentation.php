  <h3><b>New Booking Page</b></h3>
  <div>
    <ul>
        <li>Over here you can book any room you like, if it is available on the specific dates you choose.</li>
        <li>First, the fields need to be filled in, in order to view all the rooms available based on the dates you selected and you will need to enter the correct information in the proper format (Your name, email address and select the check-in and check-out dates - Note: you can not check in and check out on the same date, and neither can you choose previous dates of the month that have already passed.</li>
        <li>After filling out all the information in the correct format click on the "Show Available Rooms" button to view which all rooms are available for the dates you've picked.</li>
        <li>Since there are only 5 Villas on the entire island, if you have already booked a specific room for a given date, you will not be able to see this room in the list within that date range, all other available rooms will be displayed except for that.</li>
        <li>Under each available room, you will see the number of nights you've selected and the total price of the room for those many nights, the original price is displayed as well as the final price after the "Christmas discount of 30%" is applied.</li>
        <li>Once you click on "Book" you will receive a confirmation number on the screen, which needs to be saved/copied in order to view the details of your booking or to cancel a booking. Note: Make sure just the confirmation number is copied and no additional spaces before or after is added.</li>
    </ul>
  </div>
  
  <h3><b>Manage Bookings Page</b></h3>
  <div>
    <ul>
        <li>On this page, if you paste the confirmation code that you had previously copied, you will be able to view details of your bookings, details like your email address, the type of room you selected, the check-in and check-out dates as well as the final price.</li>
        <li>You can also see a "Cancel" button under your details, clicking on this button will successfully cancel your booking.</li>
        <li>If you try to reenter your confirmation number in the provided space again, after the cancelation process, no details will be displayed since the room was canceled.</li>
        <li>Note: When copying and pasting the confirmation code in the provided space, make sure no spaces are included before or after the number, just the confirmation number itself should be pasted.</li>
    </ul>
  </div>

