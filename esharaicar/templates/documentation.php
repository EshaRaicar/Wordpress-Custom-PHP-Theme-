        <li>On this page, you can view reviews left by previous customers, and you can also leave your review too.</li>
        <li>In order to submit/post a review, valid information needs to be entered (a name must be entered, a reaction has to be selected and a comment needs to be typed) in order to submit/post a review.</li>
        <li>After pressing/clicking on the "Post Review" button, your review should be visible underneath.</li>
    </ul>
